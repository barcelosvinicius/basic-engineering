/**
 * Generate the capabilities guide from the plugin's own frontmatter, in both
 * English (BE-GUIDE.md) and Portuguese (BE-GUIDE.pt.md).
 *
 * The catalog tables (commands / agents / skills) come from the canonical
 * English frontmatter — so they never drift. The prose (start-here, live
 * guardrails, and the Playbooks/scenarios that show HOW to use the base) is
 * localized. `--check` (run in CI / validate.js) fails if either file is stale.
 *
 * Usage:
 *   node scripts/gen-capabilities.js          # write both guides
 *   node scripts/gen-capabilities.js --check  # exit 1 if either is stale
 */
'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const PLUGIN = path.join(ROOT, 'plugins', 'be');
const GUIDE_PATH = path.join(PLUGIN, 'BE-GUIDE.md');
const GUIDE_PATH_PT = path.join(PLUGIN, 'BE-GUIDE.pt.md');

/** Minimal frontmatter parser (mirrors scripts/validate.js, CRLF-safe). */
function parseFrontmatter(content) {
  if (!content.startsWith('---')) return {};
  const end = content.indexOf('\n---', 3);
  if (end === -1) return {};
  const block = content.slice(3, end).replace(/\r/g, '');
  const result = {};
  let key = null;
  for (const raw of block.split('\n')) {
    if (!raw.trim()) continue;
    if (/^\s/.test(raw)) {
      if (key) result[key] = (result[key] + ' ' + raw.trim()).trim();
      continue;
    }
    const m = raw.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!m) continue;
    key = m[1];
    let v = m[2].trim();
    if (['>', '|', '>-', '|-'].includes(v)) v = '';
    result[key] = v.replace(/^["']|["']$/g, '');
  }
  return result;
}

function clip(text, max) {
  const t = String(text || '').replace(/\s+/g, ' ').trim();
  return t.length > max ? t.slice(0, max - 1).trimEnd() + '…' : t;
}

function readDirMd(rel) {
  const dir = path.join(PLUGIN, rel);
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((n) => n.endsWith('.md'))
    .sort()
    .map((n) => ({ file: n, fm: parseFrontmatter(fs.readFileSync(path.join(dir, n), 'utf8')) }));
}

function readSkills() {
  const dir = path.join(PLUGIN, 'skills');
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .sort()
    .map((name) => {
      const f = path.join(dir, name, 'SKILL.md');
      const fm = fs.existsSync(f) ? parseFrontmatter(fs.readFileSync(f, 'utf8')) : {};
      return { name, fm };
    });
}

