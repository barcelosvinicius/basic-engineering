function table(head, rows) {
  return [head, '| --- | --- |', ...rows].join('\n');
}

function generate(lang) {
  const t = PROSE[lang] || PROSE.en;
  const commands = readDirMd('commands');
  const agents = readDirMd('agents');
  const skills = readSkills();

  const commandRows = commands.map(({ file, fm }) => `| \`/be:${file.replace(/\.md$/, '')}\` | ${clip(fm.description, 150)} |`);
  const agentRows = agents.map(({ file, fm }) => {
    const name = fm.name || file.replace(/\.md$/, '');
    const model = fm.model ? ` _(model: ${fm.model})_` : '';
    return `| \`${name}\`${model} | ${clip(fm.description, 150)} |`;
  });

  const groups = new Map();
  for (const s of skills) {
    const prefix = s.name.includes('-') ? s.name.split('-')[0] : s.name;
    if (!groups.has(prefix)) groups.set(prefix, []);
    groups.get(prefix).push(s);
  }
  const skillSections = [...groups.keys()].sort().map((prefix) => {
    const rows = groups.get(prefix).map((s) => `| \`${s.name}\` | ${clip(s.fm.description, 150)} |`);
    return `### ${PREFIX_LABEL[prefix] || prefix}\n\n${table(t.tableHead, rows)}`;
  });

  const playRows = t.play.map(([sit, act]) => `| ${sit} | ${act} |`);

  return [
    t.note,
    '',
    t.title,
    '',
    t.intro(commands.length, agents.length, skills.length),
    '',
    t.startTitle,
    '',
    t.start,
    '',
    t.playTitle,
    '',
    t.playIntro,
    '',
    table(t.playHead, playRows),
    '',
    t.guardTitle,
    '',
    t.guardIntro,
    '',
    t.guard,
    '',
    t.optOut,
    '',
    t.commandsTitle,
    '',
    table(t.tableHead, commandRows),
    '',
    t.agentsTitle,
    '',
    t.agentsIntro,
    '',
    table(t.tableHead, agentRows),
    '',
    t.skillsTitle,
    '',
    t.skillsIntro,
    '',
    skillSections.join('\n\n'),
    '',
    '---',
    '',
    t.footer,
    '',
  ].join('\n');
}

const TARGETS = [
  { lang: 'en', file: GUIDE_PATH },
  { lang: 'pt', file: GUIDE_PATH_PT },
];

function main() {
  const check = process.argv.includes('--check');
  let stale = false;
  for (const { lang, file } of TARGETS) {
    const next = generate(lang);
    if (check) {
      const current = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : '';
      if (current.replace(/\r\n/g, '\n') !== next.replace(/\r\n/g, '\n')) {
        console.error(`gen-capabilities: ${path.basename(file)} is stale. Run \`npm run gen:guide\` and commit.`);
        stale = true;
      }
    } else {
      fs.writeFileSync(file, next);
      console.log(`gen-capabilities: wrote ${path.relative(ROOT, file)}`);
    }
  }
  if (check && stale) process.exit(1);
  if (check) console.log('gen-capabilities: guides are up to date.');
}

if (require.main === module) main();

module.exports = { generate, GUIDE_PATH, GUIDE_PATH_PT, TARGETS };
